// Helper function to get a specific translation string
export const getText = (language, section, key) => {
  try {
    return translations[language][section][key];
  } catch (error) {
    console.warn(`Translation not found for: ${language}.${section}.${key}`);
    // Fallback to English
    try {
      return translations.en[section][key];
    } catch {
      return `[Missing: ${section}.${key}]`;
    }
  }
};
